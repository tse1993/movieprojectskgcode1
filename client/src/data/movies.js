// Φιλτράρισμα ανά genre
export function getMoviesByGenre(genre) {
  if (genre === "All") return movies;
  return movies.filter((movie) => movie.genre === genre);
}

// Αναζήτηση ταινιών
export function searchMovies(query) {
  if (!query.trim()) return movies;
  const lowerQuery = query.toLowerCase();
  return movies.filter(
    (movie) =>
      movie.title.toLowerCase().includes(lowerQuery) ||
      movie.genre.toLowerCase().includes(lowerQuery) ||
      movie.overview.toLowerCase().includes(lowerQuery)
  );
}

// Δημοφιλείς ταινίες (βασισμένο σε rating >= 8.0)
export function getPopularMovies() {
  return movies
    .filter((movie) => movie.rating >= 8.0)
    .sort((a, b) => b.rating - a.rating);
}

// Top rated (ταξινόμηση κατά rating φθίνουσα)
export function getTopRatedMovies() {
  return movies.slice().sort((a, b) => b.rating - a.rating);
}

// New releases (ταξινόμηση κατά ημερομηνία κυκλοφορίας φθίνουσα)
export function getNewReleases() {
  return movies
    .slice()
    .sort(
      (a, b) =>
        new Date(b.releaseDate).getTime() - new Date(a.releaseDate).getTime()
    );
}
